Maps = function (id, imgSrc, grid) {
  const self = {
    id: id,
    image: imgSrc,
    width: grid[0].length * TILE_SIZE,
    height: grid.length * TILE_SIZE,
    grid: grid
  };

  self.generateCoffins = function () {
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "coffin") {
          new Coffin(i2, i, self);
        }
      }
    }
  };

  self.generateCoffins();

  //.......................TORCHES.....................................
  self.generateTorches = function () {
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "torch") {
          new Torch(i2, i, self);
        }
      }
    }
  };

  self.generateTorches();
  //................................TRAPS.....................................


  self.generateTraps = function () {
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "trap") {
          new Trap(i2, i, true, self);
        }
      }
    }
  };

  self.generateTraps()
  //.......................................Triggers...............................
  self.generateTriggers = function () {
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "trigger") {
          new Trigger(i2, i, self);
        }
      }
    }
  };

  self.generateTriggers();



  //...................................Artifacts....................................
  self.generateArtifacts = function () {
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "darkBook") {
          new Artifact("darkBook", i2, i, self);
        }
      }
    }
  };

  self.generateArtifacts();
  //........................................Enemies.........................................
  self.generateEnemies = function () {
    //0.1 GoblinCult; 
    //0.2 GiantEye;
    for (let i = 0; i < self.grid.length; i++) {
      for (let i2 = 0; i2 < self.grid[0].length; i2++) {
        if (self.grid[i][i2] === "GoblinCultist") {
          new Enemy(enemyBook["GoblinLightningCultist"], i2 * TILE_SIZE, i * TILE_SIZE, self);
        } else if (self.grid[i][i2] === "GiantEye") {
          new Enemy(enemyBook["GiantEye"], i2 * TILE_SIZE, i * TILE_SIZE, self);
        }
      }
    }
  };

  self.generateEnemies()
  //...................................................................................
  self.isPositionWall = function (pt) {
    let gridX = Math.floor(pt.x / TILE_SIZE);
    let gridY = Math.floor(pt.y / TILE_SIZE);
    if (gridX < 0 || gridX >= self.grid[0].length) {
      return true;
    }
    if (gridY < 0 || gridY >= self.grid.length) {
      return true;
    }
    if (
      self.grid[gridY][gridX] === 1 ||
      self.grid[gridY][gridX] === "torch" ||
      self.grid[gridY][gridX] === "coffin" ||
      self.grid[gridY][gridX] === "trigger"
    ) {
      return true;
    } else if (self.grid[gridY][gridX] === 0) {
      return false;
    } else {
      return false;
    }
  };

  mapList[id] = self;
  return self;
};
